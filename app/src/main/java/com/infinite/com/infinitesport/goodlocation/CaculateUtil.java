    /**
     * 根据不同阶段的速度显示轨迹颜色
     */
    public static List<Integer> caculateSpeedColor(List<TrackPoint> list) {
        List<Integer> colorList = new ArrayList<Integer>();
        for (int i = 0, n = list.size(); i < n; i++) {
            if (i + 1 < n) {
                TrackPoint p1 = list.get(i);
                TrackPoint p2 = list.get(i + 1);
                float s = CaculateUtil.caculateSpeedWithKm(new LatLng(p1.getLat(), p1.getLng()), new LatLng(p2.getLat(), p2.getLng()), (int) (p2.getTime() - p1.getTime()));
                Log.i(TAG, "----speed: " + s);
                if (s >= 0 && s < 8) {
                    colorList.add(colors[0]);
                }
                if (s >= 8 && s < 9) {
                    colorList.add(colors[1]);
                }
                if (s >= 9) {
                    colorList.add(colors[2]);
                }
            }
        }
        return colorList;
    }


